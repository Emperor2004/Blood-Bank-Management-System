create database bloodbank;

use bloodbank;
-- 1. BloodBanks: Holds details of each blood bank.
CREATE TABLE BloodBanks (
    bloodBankID INT AUTO_INCREMENT PRIMARY KEY,
    bankName VARCHAR(100) NOT NULL,
    address VARCHAR(200) NOT NULL,
    city VARCHAR(100),
    state VARCHAR(100),
    contact VARCHAR(50)
);

-- 2. BloodInventory: Tracks the inventory per blood bank and blood type.
CREATE TABLE BloodInventory (
    bloodBankID INT,
    bloodType VARCHAR(5),
    quantity INT NOT NULL,
    PRIMARY KEY (bloodBankID, bloodType),
    FOREIGN KEY (bloodBankID) REFERENCES BloodBanks(bloodBankID)
);

-- 3. Donors: Stores donor information.
CREATE TABLE Donors (
    donorID INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    bloodType VARCHAR(5) NOT NULL,
    address VARCHAR(200),
    city VARCHAR(100),
    state VARCHAR(100),
    contact VARCHAR(50)
);

-- 4. Recipients: Stores recipient information.
CREATE TABLE Recipients (
    recipientID INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    bloodType VARCHAR(5) NOT NULL,
    address VARCHAR(200),
    city VARCHAR(100),
    state VARCHAR(100),
    contact VARCHAR(50)
);

-- 5. DonationEvents: Logs each donation event.
CREATE TABLE DonationEvents (
    donationID INT AUTO_INCREMENT PRIMARY KEY,
    donorID INT NOT NULL,
    bloodBankID INT NOT NULL,
    bloodType VARCHAR(5) NOT NULL,
    quantity INT NOT NULL,
    donationDate DATE NOT NULL,
    FOREIGN KEY (donorID) REFERENCES Donors(donorID),
    FOREIGN KEY (bloodBankID) REFERENCES BloodBanks(bloodBankID)
);

-- 6. BloodRequests: Records blood requests along with location and assignment details.
CREATE TABLE BloodRequests (
    requestID INT AUTO_INCREMENT PRIMARY KEY,
    recipientID INT,  -- Optional if the requester is registered as a recipient
    recipientName VARCHAR(100) NOT NULL,
    bloodType VARCHAR(5) NOT NULL,
    requiredQuantity INT NOT NULL,
    requestDate DATE NOT NULL,
    address VARCHAR(200),
    city VARCHAR(100),
    state VARCHAR(100),
    contact VARCHAR(50),
    status VARCHAR(20) DEFAULT 'Pending',  -- e.g., Pending, Approved, Rejected
    assignedBloodBankID INT,
    FOREIGN KEY (recipientID) REFERENCES Recipients(recipientID),
    FOREIGN KEY (assignedBloodBankID) REFERENCES BloodBanks(bloodBankID)
);

-- 7. BloodTransfers: Captures transfers between blood banks.
CREATE TABLE BloodTransfers (
    transferID INT AUTO_INCREMENT PRIMARY KEY,
    fromBankID INT NOT NULL,
    toBankID INT NOT NULL,
    bloodType VARCHAR(5) NOT NULL,
    quantity INT NOT NULL,
    transferDate DATE NOT NULL,
    FOREIGN KEY (fromBankID) REFERENCES BloodBanks(bloodBankID),
    FOREIGN KEY (toBankID) REFERENCES BloodBanks(bloodBankID)
);

-- 8. Staff: Stores staff credentials and details.
CREATE TABLE Staff (
    staffID VARCHAR(50) PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    password VARCHAR(50) NOT NULL,
    contact VARCHAR(50),
    role VARCHAR(50)  -- e.g., Manager, Technician, etc.
);

